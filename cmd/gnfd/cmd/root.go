// NewRootCmd creates a new root command for a Cosmos SDK application
func NewRootCmd() (*cobra.Command, appparams.EncodingConfig) {
	encodingConfig := app.MakeEncodingConfig()
	initClientCtx := client.Context{}.
		WithCodec(encodingConfig.Marshaler).
		WithInterfaceRegistry(encodingConfig.InterfaceRegistry).
		WithTxConfig(encodingConfig.TxConfig).
		WithLegacyAmino(encodingConfig.Amino).
		WithKeyringOptions(keyring.ETHAlgoOption()).
		WithInput(os.Stdin).
		WithAccountRetriever(types.AccountRetriever{}).
		WithHomeDir(app.DefaultNodeHome).
		WithViper("")

	rootCmd := &cobra.Command{
		Use:   app.ShortName,
		Short: "Stargate CosmosHub App",
		PersistentPreRunE: func(cmd *cobra.Command, _ []string) error {
			// set the default command outputs
			cmd.SetOut(cmd.OutOrStdout())
			cmd.SetErr(cmd.ErrOrStderr())
			initClientCtx, err := client.ReadPersistentCommandFlags(initClientCtx, cmd.Flags())
			if err != nil {
				return err
			}
			initClientCtx, err = config.ReadFromClientConfig(initClientCtx)
			if err != nil {
				return err
			}

			if err := client.SetCmdClientContextHandler(initClientCtx, cmd); err != nil {
				return err
			}

			customTMConfig := initTendermintConfig()
			err = server.InterceptConfigsPreRunHandler(
				cmd, app.CustomAppTemplate, app.NewDefaultAppConfig(), customTMConfig,
			)
			if err != nil {
				return err
			}

			return ParseAppConfigInPlace(cmd)
		},
	}

	initRootCmd(rootCmd, encodingConfig)
	overwriteFlagDefaults(rootCmd, map[string]string{
		flags.FlagChainID:        app.Name + "_" + app.EIP155ChainID + "-" + app.Epoch,
		flags.FlagKeyringBackend: "test",
		flags.FlagSignMode:       flags.SignModeEIP712,
		flags.FlagGas:            "auto",
	})

	return rootCmd, encodingConfig
}

func ParseAppConfigInPlace(cmd *cobra.Command) error {
	newViper := viper.New()

	// Configure the viper instance
	if err := newViper.BindPFlags(cmd.Flags()); err != nil {
		return err
	}
	if err := newViper.BindPFlags(cmd.PersistentFlags()); err != nil {
		return err
	}

	homeDir := newViper.GetString(flags.FlagHome)

	newViper.SetConfigName("app")
	newViper.SetConfigType("toml")
	newViper.AddConfigPath(homeDir)
	newViper.AddConfigPath(filepath.Join(homeDir, "config"))

	// If a config file is found, read it in.
	if err := newViper.ReadInConfig(); err != nil {
		return err
	}

	appConfig = app.NewDefaultAppConfig()
	err := newViper.Unmarshal(appConfig)
	if err != nil {
		return err
	}

	srvCfg := serverconfig.DefaultConfig()
	err = newViper.Unmarshal(srvCfg)
	if err != nil {
		return err
	}
	appConfig.Config = *srvCfg

	return nil
}

// initTendermintConfig helps to override default Tendermint Config values.
// return tmcfg.DefaultConfig if no custom configuration is required for the application.
func initTendermintConfig() *tmcfg.Config {
	cfg := tmcfg.DefaultConfig()
	return cfg
}

func initRootCmd(
	rootCmd *cobra.Command,
	encodingConfig appparams.EncodingConfig,
) {
	// Set config
	initSDKConfig()

	rootCmd.AddCommand(
		genutilcli.InitCmd(app.ModuleBasics, app.DefaultNodeHome),
		genutilcli.CollectGenTxsCmd(banktypes.GenesisBalancesIterator{}, app.DefaultNodeHome),
		genutilcli.MigrateGenesisCmd(),
		genutilcli.GenTxCmd(
			app.ModuleBasics,
			encodingConfig.TxConfig,
			banktypes.GenesisBalancesIterator{},
			app.DefaultNodeHome,
		),
		genutilcli.ValidateGenesisCmd(app.ModuleBasics),
		AddGenesisAccountCmd(app.DefaultNodeHome),
		tmcli.NewCompletionCmd(rootCmd, true),
		debug.Cmd(),
		config.Cmd(),
		// this line is used by starport scaffolding # root/commands
	)

	a := appCreator{
		encodingConfig,
	}

	// add server commands
	server.AddCommands(
		rootCmd,
		app.DefaultNodeHome,
		a.newApp,
		a.appExport,
		addModuleInitFlags,
	)

	// add keybase, auxiliary RPC, query, and tx child commands
	rootCmd.AddCommand(
		rpc.StatusCommand(),
		queryCommand(),
		txCommand(),
		keys.Commands(app.DefaultNodeHome),
		startWithTunnelingCommand(a, app.DefaultNodeHome),
	)

	overrideOrAppendCommand(rootCmd, map[string]*cobra.Command{
		"version": versionCommand(),
	})
}

func overrideOrAppendCommand(cmd *cobra.Command, overrides map[string]*cobra.Command) {
	for _, subCmd := range cmd.Commands() {
		if _, ok := overrides[subCmd.Use]; ok {
			cmd.RemoveCommand(subCmd)
		}
	}

	for _, newCmd := range overrides {
		cmd.AddCommand(newCmd)
	}
}

func versionCommand() *cobra.Command {
	return &cobra.Command{
		Use:   "version",
		Short: "Print the application binary version information",
		RunE: func(cmd *cobra.Command, _ []string) error {
			fmt.Print(version.Version())
			return nil
		},
	}
}

